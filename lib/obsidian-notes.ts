/** Makni znakove nedopuštene u nazivima fajlova (Windows + Obsidian) i wikilink konflikte. */
export function safeName(s: string): string {
  return (s || 'Projekt')
    .replace(/[\\/:*?"<>|#^[\]]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 80) || 'Projekt';
}

